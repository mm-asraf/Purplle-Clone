function displayProducts(){

    allProducts.forEach((product) => {
        let div = document.createElement("div");
        let img = document.createElement("img");
        let title = document.createElement("p");
        let discounted_price = document.createElement("p");
        let original_price = document.createElement("p");
        let off_percentage = document.createElement("p");
        let rating = document.createElement("p");
        let shades = document.createElement("div");
        let shade_1 = document.createElement("img");
        let shade_2 = document.createElement("img");
        let shade_3 = document.createElement("img");
        let shade_4 = document.createElement("img");
        let shade_5 = document.createElement("img");
        let price = document.createElement("div");

        title.style.whiteSpace = "nowrap";
        title.style.overflow = "hidden";
        title.style.textOverflow = "ellipsis"; 
        div.style.cursor = "pointer";

        img.src = product.img;
        title.innerText = product.title;
        discounted_price.innerText = product.discountedPrice;
        original_price.innerText = product.originalPrice;
        off_percentage.innerText = product.offPercentage;
        rating.innerText = product.rating;

        title.setAttribute("id", "mainTitle");
        rating.setAttribute("id", "rating");
        discounted_price.setAttribute("id", "discountedPrice");
        original_price.setAttribute("id", "originalPrice");
        off_percentage.setAttribute("id", "offPercentage");
        price.setAttribute("id", "price");

        shade_1.src = product.shadeOne;
        shade_2.src = product.shadeTwo;
        shade_3.src = product.shadeThree;
        shade_4.src = product.shadeFour;
        shade_5.src = product.shadeFive;

        shade_1.setAttribute("class", "shade");
        shade_2.setAttribute("class", "shade");
        shade_3.setAttribute("class", "shade");
        shade_4.setAttribute("class", "shade");
        shade_5.setAttribute("class", "shade");

       div.onclick = function(){
            appendProduct(product);
            window.location.href = "ogproductPage.html";
        }
        div.setAttribute("id", "displayInfo");
        shades.setAttribute("class", "shades");

        shades.append(shade_1, shade_2, shade_3, shade_4, shade_5);
        price.append(discounted_price, original_price, off_percentage)
        div.append(img, title, price, rating, shades);
        display.append(div);
    });
}

displayProducts();

function appendProduct(product){
    if(localStorage.getItem("showProduct") === null)
        {
            localStorage.setItem("showProduct", JSON.stringify([]));
        }
   let show_product = JSON.parse(localStorage.getItem("showProduct"));
   show_product.push(product);
   localStorage.setItem("showProduct", JSON.stringify(show_product));
}

